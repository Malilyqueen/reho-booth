/**
 * Recalcule le budget total Ã  partir des lignes de dÃ©penses
 */
function recalculateTotalBudget() {
    let total = 0;
    
    // Parcourir toutes les lignes de dÃ©pense pour calculer le total
    const expenseLines = document.querySelectorAll('.expense-line-amount');
    expenseLines.forEach(line => {
        const amount = parseAmount(line.value);
        if (!isNaN(amount)) {
            total += amount;
        }
    });
    
    // Mettre Ã  jour l'affichage du total
    updateTotalBudget(total);
    
    return total;
}

