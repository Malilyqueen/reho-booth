/**
 * Parse un montant depuis une chaÃ®ne de caractÃ¨res
 * @param {string} amountStr - La chaÃ®ne Ã  parser
 * @returns {number} - Le montant en nombre
 */
function parseAmount(amountStr) {
    if (!amountStr) return 0;
    
    // Supprimer tout ce qui n'est pas un chiffre, un point ou une virgule
    const cleanedStr = amountStr.toString().replace(/[^0-9.,]/g, '');
    
    // Remplacer la virgule par un point pour la conversion
    const normalized = cleanedStr.replace(',', '.');
    
    // Convertir en nombre
    const amount = parseFloat(normalized);
    
    return isNaN(amount) ? 0 : amount;
}

/**
 * Formate un montant en devise
 * @param {number} amount - Le montant Ã  formater
 * @returns {string} - La chaÃ®ne formatÃ©e
 */
function formatCurrency(amount) {
    // S'assurer que amount est un nombre
    const numAmount = parseFloat(amount) || 0;
    
    // RÃ©cupÃ©rer le symbole de devise actuel
    const currencySymbol = window.currencySymbol || 'â‚¬';
    
    // Formater avec 2 dÃ©cimales fixes
    return currencySymbol + ' ' + numAmount.toFixed(2).replace('.', ',');
}

